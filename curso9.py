var = 17
var_right = var >> 1
var_left = var << 2
print(var, var_left, var_right)
#17 >> 1 -> 17 // 2 (17 dividido por 2 à potência de 1) -> 8 (deslocar para a direita por um bit é igual a divisão inteira por 2)
#17 << 2 -> 17 * 4 (17 multiplicado por 2 à potência de 2) -> 68 (deslocar paraa esquerda dois bits é igual a multiplicação do número inteiro por quatro)
